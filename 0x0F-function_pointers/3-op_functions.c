#include "3-calc.h"

/**
 * op_add - adds two integers
 * @a: int one
 * @b: int two
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts two integers
 * @a: the first int
 * @b; int two
 * Return: the difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication of two numbers
 * @a: the first int
 * @b: int two
 * Return: the mult
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two integrs
 * @a: the first int
 * @b: second int
 * Return; quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo of two int
 * @a: the first int
 * @b: int two
 * Return: the remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
